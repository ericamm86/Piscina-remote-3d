export function formatLength(value) {
  return `${value.toFixed(1)} m`;
}

export function formatArea(value, language) {
  return language === "en" ? `${value.toFixed(1)} sq m` : `${value.toFixed(1)} m²`;
}

export function formatCurrency(value, language) {
  return value.toLocaleString(language === "en" ? "en-US" : "pt-BR", {
    style: "currency",
    currency: "BRL"
  });
}
